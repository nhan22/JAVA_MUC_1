    public ActionListener createAction(JButton button){
        ActionListener a1 = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if (button.getText().equals("") && !getTheWinner()){
                    if (x_or_o % 2 == 0){
                        button.setText("X");
                        button.setForeground(Color.blue);
                        if (getTheWinner()){
                            winX++;
                            txtWinOfNumberX.setText("" + winX);
                        }
                        getTheDraw();
                    }else{
                        button.setText("O");
                        button.setForeground(Color.red);
                        if (getTheWinner()){
                            winO++;
                            txtWinOfNumberO.setText("" + winO);
                        }
                        getTheDraw();
                    }
                    x_or_o++;
                }
            }
            
        };
        return a1;
    }
    
