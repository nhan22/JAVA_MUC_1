    public boolean getTheWinner(){
        String[][] board = new String[3][3];
        board[0][0] = bt1.getText();
        board[0][1] = bt2.getText();
        board[0][2] = bt3.getText();
        board[1][0] = bt4.getText();
        board[1][1] = bt5.getText();
        board[1][2] = bt6.getText();
        board[2][0] = bt7.getText();
        board[2][1] = bt8.getText();
        board[2][2] = bt9.getText();

        // Kiểm tra các hàng
        for (int i = 0; i < 3; i++) {
            if (!board[i][0].equals("") && board[i][0].equals(board[i][1]) && board[i][1].equals(board[i][2])) {
                return true;
            }
        }

        // Kiểm tra các cột
        for (int j = 0; j < 3; j++) {
            if (!board[0][j].equals("") && board[0][j].equals(board[1][j]) && board[1][j].equals(board[2][j])) {
                return true;
            }
        }

        // Kiểm tra đường chéo chính
        if (!board[0][0].equals("") && board[0][0].equals(board[1][1]) && board[1][1].equals(board[2][2])) {
            return true;
        }

        // Kiểm tra đường chéo phụ
        if (!board[0][2].equals("") && board[1][1].equals(board[0][2]) && board[2][0].equals(board[0][2])) {
            return true;
        }
        return false;
    }
    
